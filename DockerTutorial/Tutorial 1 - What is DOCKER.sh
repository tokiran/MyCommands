#Tutorial3 - Benifits of 
	#Build app only once: 
		- An application inside a container can run on any system that has Docker installed. So there is no need to build and configure app multiple times on different platforms

	#More sleep and less worry: 
		- With Docker you test your application inside a container and ship it inside a container. This means the environment in which you test is identical to the one on which the app will run in production.

	#Portability: 
		- Docker containers can run on any platform. It can run on your local system, Amazon ec2, Goolge Cloud platform, Rackspace server, VirtualBox..etc.

	#Version Control:
		- Like Git, Docker has in-built version control system. Docker containers work just likt GIT repositories, allowing your to commit changes to your docker images and version control them.

	#Isolation: 
		- With Docker every application works in isolation in its own container and does not interferes with other applications running on the same system.

	#Productivity: 
		- Docker allows faster and more efficient deployments without worrying about running your app on different platforms. It increases productivity many folds.

